
# Program to evaluate days of the week
day = int(input("Enter Day (1-7):"))
if day == 1:
    print("Sunday")
elif day == 2:
    print("Monday")
elif day == 3:
    print("Tuesday")
elif day == 4:
    print("Wednesday")
elif day == 5:
    print("Thursday")
elif day == 6:
    print("friday")
elif day == 7:
    print("saturday")
else:
    print("Invalid Input")